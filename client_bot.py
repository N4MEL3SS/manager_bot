@client_router.message(F.text)
async def handle_question(message: Message):
    """–û–±—Ä–∞–±–æ—Ç–∫–∞ –≤–æ–ø—Ä–æ—Å–æ–≤ –æ—Ç –∫–ª–∏–µ–Ω—Ç–æ–≤."""
    try:
        if len(message.text) > config.MAX_TICKET_LENGTH:
            await message.answer(
                f"‚ùå –í–∞—à –≤–æ–ø—Ä–æ—Å —Å–ª–∏—à–∫–æ–º –¥–ª–∏–Ω–Ω—ã–π. –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –¥–ª–∏–Ω–∞: {config.MAX_TICKET_LENGTH} —Å–∏–º–≤–æ–ª–æ–≤."
            )
            return

        # –ü–æ–ª—É—á–∞–µ–º –Ω–∏–∫–Ω–µ–π–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        nickname = message.from_user.username
        if not nickname:
            nickname = f"{message.from_user.first_name or ''} {message.from_user.last_name or ''}".strip()
            if not nickname:
                nickname = "–ê–Ω–æ–Ω–∏–º–Ω—ã–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å"

        # –°–æ–∑–¥–∞–µ–º —Ç–∏–∫–µ—Ç
        ticket = await db.create_ticket(client_chat_id=message.chat.id, client_nickname=nickname, question=message.text)

        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ
        confirmation_text = f"""
‚úÖ –í–∞—à –≤–æ–ø—Ä–æ—Å –ø—Ä–∏–Ω—è—Ç! –ù–æ–º–µ—Ä –∑–∞—è–≤–∫–∏: #{ticket.id}

–ú—ã –ø–æ–ª—É—á–∏–ª–∏ –≤–∞—à –≤–æ–ø—Ä–æ—Å –∏ —Å–∫–æ—Ä–æ –Ω–∞ –Ω–µ–≥–æ –æ—Ç–≤–µ—Ç–∏–º. –û–∂–∏–¥–∞–π—Ç–µ, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞.

–í–∞—à –≤–æ–ø—Ä–æ—Å:
{message.text}
        """
        await message.answer(confirmation_text)

        logger.info(f"New question from {nickname}: {message.text[:50]}...")

        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –º–µ–Ω–µ–¥–∂–µ—Ä–∞–º
        asyncio.create_task(notification_manager.notify_new_ticket(ticket))

    except Exception as e:
        logger.error(f"Error handling question: {e}")
        await message.answer("‚ùå –ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –≤–∞—à–µ–≥–æ –≤–æ–ø—Ä–æ—Å–∞. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ.")


async def run_client_bot():
    """–ó–∞–ø—É—Å–∫ –∫–ª–∏–µ–Ω—Ç—Å–∫–æ–≥–æ –±–æ—Ç–∞."""
    bot = Bot(token=config.CLIENT_BOT_TOKEN)
    dp = Dispatcher()
    dp.include_router(client_router)

    await dp.start_polling(bot)
